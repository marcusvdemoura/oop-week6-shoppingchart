package com.company;

public class Products {


    private int oranges;
    private int apples;


    public Products(int oranges, int apples) {
        this.oranges = oranges;
        this.apples = apples;
    }

    public int getOranges() {
        return oranges;
    }

    public int getApples() {
        return apples;
    }
}
